package sidecar

import (
	"encoding/json"
	"fmt"
	"log"
	"net/http"
)

// Server provides HTTP API for the sidecar
type Server struct {
	service *AgentService
	port    int
}

// NewServer creates a new HTTP server
func NewServer(workspaceDir string, port int) *Server {
	return &Server{
		service: NewAgentService(workspaceDir),
		port:    port,
	}
}

// Start starts the HTTP server
func (s *Server) Start() error {
	http.HandleFunc("/health", s.handleHealth)
	http.HandleFunc("/files", s.handleFiles)
	http.HandleFunc("/execute", s.handleExecute)
	http.HandleFunc("/signal", s.handleSignal)
	http.HandleFunc("/reset", s.handleReset)

	addr := fmt.Sprintf(":%d", s.port)
	log.Printf("Sidecar server starting on %s", addr)
	return http.ListenAndServe(addr, nil)
}

func (s *Server) handleHealth(w http.ResponseWriter, r *http.Request) {
	w.WriteHeader(http.StatusOK)
	json.NewEncoder(w).Encode(map[string]string{"status": "healthy"})
}

func (s *Server) handleFiles(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
		return
	}

	var req FileRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	resp, err := s.service.UpdateFiles(r.Context(), &req)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(resp)
}

